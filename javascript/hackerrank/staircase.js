function staircase(n){
    for (let row = 1; row <= n; row++) {
        let output = '';
        for (let col = 0;col <n-row; col ++) {
            output += ' ';
        }
        for (let x =0;x <row; x ++) {
            output += '#';
        }
        console.log(output);
    }

}

